/**
 * op_add - add two numbers
 * @a: num 1
 * @b: num 2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - sub two numbers
 * @a: num 1
 * @b: num 2
 * Return: sub
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - mutliply two numbers
 * @a: num 1
 * @b: num 2
 * Return: mul
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - devide two numbers
 * @a: num 1
 * @b: num 2
 * Return: div
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - modulus two numbers
 * @a: num 1
 * @b: num 2
 * Return: mod
 */
int op_mod(int a, int b)
{
	return (a % b);
}
